package com.haridroid.dayremarker;

public class itemStructure {
    String month, day, note, dayOfWeek;
    public itemStructure(String month, String day, String note, String dayOfWeek) {
        this.month = month;
        this.day = day;
        this.note = note;
        this.dayOfWeek = dayOfWeek;
    }
}
